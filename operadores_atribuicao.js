let numero = 10;

console.log(`O numero ínicial é ${numero}`);
console.log(`Somando 10: ${numero += 10}`);
console.log(`Subraindo 10: ${numero -= 10}`);
console.log(`Multiplicando por 10: ${numero *= 10}`);
console.log(`Dvidido por 10: ${numero /= 10}`);
console.log(`Obtendo o resto da divisão por 10: ${numero %= 10}`);
console.log(`Elevando a potência de 10: ${numero **= 10}`);
console.log(`Incrementado 1: ${++numero }`);
console.log(`Decremento 1: ${--numero }`);
console.log(`Numero final é: ${numero }`);

